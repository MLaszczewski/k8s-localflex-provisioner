package cmd

import (
	"fmt"
	"os"

	"github.com/spf13/cobra"
)

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "localflex",
	Short: "localflex driver that creates a local path and a symlink to the pod",
	Long: `This Flexvolume Driver for Kubernetes creates a local folder set through a parameter
passed to the "mount" command. It is used as kubelet plugin if the correct flexvolume driver is
defined in the Persistent Volume.
`,
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	if err := rootCmd.Execute(); err != nil {
		fmt.Println(err)
		os.Exit(1)
	}
}
